function normalizeItemText(item) {
  if (!item) return '';
  return item.title + ': ' + item.body;
}

function collectVisiblePoints(slide) {
  const items = slide.cards || slide.steps || [];
  return items.slice(0, 3).map(normalizeItemText).filter(Boolean);
}

function buildSpeakerScript(slide) {
  if (currentPresenterScripts[slide.id]) return currentPresenterScripts[slide.id];
  if (customSpeakerScripts[slide.id]) return customSpeakerScripts[slide.id];

  const message = slide.keyMessage || slide.subtitle || 'هذه الشريحة تساعدنا على فهم فكرة عملية مرتبطة بالورشة.';
  const points = collectVisiblePoints(slide);
  const pointsParagraph = points.length
    ? '\n\nالنقاط الظاهرة أمامنا توضح الفكرة من أكثر من زاوية. ' + points.join(' ')
    : '';
  const bridge = slide.type === 'hero'
    ? 'ومن هنا نبدأ الرحلة من الفهم العام إلى التطبيق العملي.'
    : 'وسنستخدم هذه الفكرة لاحقًا لفهم كيف يتحول الذكاء الاصطناعي من عنوان عام إلى أداة نافعة في الدراسة والعمل الفني.';

  return speakerPrefix + '\nفي هذه الشريحة نركز على فكرة: ' + slide.title + '. الرسالة الأساسية هي: ' + message + pointsParagraph + '\n\nالمهم هنا أن لا نتعامل مع الذكاء الاصطناعي ككلمة جذابة فقط، بل كأداة تحتاج سؤالًا واضحًا وبيانات مناسبة ومراجعة بشرية. عندما يفهم المشارك أو المهندس هذه الفكرة، يصبح قادرًا على استخدام التقنية بوعي بدل الاعتماد عليها بشكل أعمى. ' + bridge;
}

